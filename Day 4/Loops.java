public class Loops {
    public static void main(String[] args) {

        //For Loop Example
        int i =1;
        for(i=1;i<=20;i=i+2){
            System.out.println("Odd Number: "+i);
        }


        // While Loop Example
        int j = 2;
        while(j<=20){
            System.out.println("Even Number:  "+j);
            j =j +2;
        }


        // Do-While Loop Example
        int k = 1;
        do{
            System.out.println(" Number:"+k);
            k++;
        }while(k<=20);
        
    }
}
